package Ques22;

public class LowerUpper {

	public static void main(String[] args) {
		String s1 = "write a program to convert lower case into upper case";
		String s2 = s1.toUpperCase();
		System.out.println("Lower to Upper case is " + s2);
		String s3 = "WRITE A PROGRAM TO CONVERT LOWER CASE INTO UPPER CASE";
		String s4 = s3.toLowerCase();
		System.out.println("Upper to Lower case is " + s4);
	}

}
